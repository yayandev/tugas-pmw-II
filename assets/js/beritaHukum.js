function showBeritaHukum() {
  let content = document.getElementById("content");

  for (let x = 0; x < BeritaHukum.length; x++) {
    let a = document.createElement("a");
    a.setAttribute("class", "card");
    a.setAttribute("href", BeritaHukum[x].link);
    a.setAttribute("target", "_blank");
    let img = document.createElement("img");
    img.setAttribute("src", BeritaHukum[x].thumbnail);
    img.setAttribute("alt", "GAGAL MEMUAT IMG PASTIKAN INTERNET ANDA AKTIF!");
    let h4 = document.createElement("h4");
    h4.innerText = BeritaHukum[x].title;
    let p = document.createElement("p");
    p.innerText = BeritaHukum[x].pubDate.split("T")[0];

    a.append(img, h4, p);
    content.append(a);
  }
}

showBeritaHukum();
